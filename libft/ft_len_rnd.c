#include "libft.h"

size_t		ft_len_rnd(char const *s, char c, size_t i)
{
	size_t	j;

	j = i;
	while (s[j] != c && s[j] != '\0')
	{
		j++;
		if (s[j] == c || s[j] == '\0')
			return (j - i);
	}
	return (0);
}
